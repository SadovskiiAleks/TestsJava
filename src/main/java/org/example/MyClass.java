package org.example;

public class MyClass {

    public int add(int a, int b) {
        return a + b;
    }

    public int square(int a) {
        return a * a;
    }

    public int cube(int a) {
        return a * a * a;
    }

    public String toString(int a) {
        String string = "Вводимое число " + a + " !";
        return string;
    }
}
